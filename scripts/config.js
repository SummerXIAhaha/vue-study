function genConfig (name) {
  const opts = builds[name]
  const config = {
    input: opts.entry, // 设置打包入口
    external: opts.external, // 应该保留在bundle外部引用的模块ID
    plugins: [ // 设置插件
      flow(), // 清楚静态类型检查代码
      alias(Object.assign({}, aliases, opts.alias)) // 设置简短别名
    ].concat(opts.plugins || []),  // 结合配置项中间的plugins
    output: {
      file: opts.dest,
      format: opts.format,
      banner: opts.banner,
      name: opts.moduleName || 'Vue'
    },
    onwarn: (msg, warn) => {
      if (!/Circular/.test(msg)) {
        warn(msg)
      }
    } // 拦截警告信息
  }

  // built-in vars
  const vars = {
    __WEEX__: !!opts.weex,
    __WEEX_VERSION__: weexVersion,
    __VERSION__: version
  }
  // feature flags //
  Object.keys(featureFlags).forEach(key => {
    vars[`process.env.${key}`] = featureFlags[key]
  })
  // build-specific env opts.env: development or production
  if (opts.env) {
    vars['process.env.NODE_ENV'] = JSON.stringify(opts.env)
  }
  /*
  vars = {
    __WEEX__: !!opts.weex,
    __WEEX_VERSION__: weexVersion,
    __VERSION__: version,
    process.env.NEW_SLOT_SYNTAX: true,
    process.env.VBIND_PROP_SHORTHAND: false,
    process.env.NODE_ENV: development or production,
  }
  */
  // 使用rollup-plugin-replace来查找和替换目前的环境变量
  config.plugins.push(replace(vars))

  // 判断是否需要将ES6 转成ES5
  if (opts.transpile !== false) {
    config.plugins.push(buble())
  }
  // 定义生成包名称
  Object.defineProperty(config, '_name', {
    enumerable: false,
    value: name
  })

  return config
}

// 判断环境变量是否有TARGET参数
if (process.env.TARGET) {
  // 通过TARGET参数获取相应的打包配置项
  module.exports = genConfig(process.env.TARGET)
} else {
  // 暴露获取打包方式的方法（传入builds的key值就能获取到相应的配置项）
  exports.getBuild = genConfig
  // 暴露所有的打包方法
  exports.getAllBuilds = () => Object.keys(builds).map(genConfig)
}
